    public void tabelUser() {
       Object[] Baris = {"No","Id", "Username", "Password", "Level"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelUser.setModel(tabmode);
        String sql = "select * from tb_user order by id asc";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String id = hasil.getString("id");
                String username = hasil.getString("username");
                String password = hasil.getString("password");
                String level = hasil.getString("level");
                String[] data = {"", id, username, password, level};
                tabmode.addRow(data);
                noTable();
                lebarKolom();
            }
        } catch (Exception e) {
        }
} 

