    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
         if (txtId.getText().equals("")) {
    JOptionPane.showMessageDialog(null, "Id tidak boleh kosong");
} else if (txtUsername.getText().equals("")) {
    JOptionPane.showMessageDialog(null, "Username Barang tidak boleh kosong");
} else if (txtPassword.getText().equals("")) {
    JOptionPane.showMessageDialog(null, "Password tidak boleh kosong");
}  else {
    String sql = "insert into tb_user values (?,?,?,?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtId.getText());
        stat.setString(2, txtUsername.getText());
        stat.setString(3, txtPassword.getText());
        stat.setString(4, (String) cmbLevel.getSelectedItem());
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        kosong();
        tabelUser();
        lebarKolom();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan" + e);
    }
}
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnSimpanUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanUbahActionPerformed
        String sql = "update tb_User set id=?,username=?,password=?,level=? where id='" + txtIdUbah.getText() + "'";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtIdUbah.getText());
            stat.setString(2, txtUsernameUbah.getText());
            stat.setString(3, txtPasswordUbah.getText());
            stat.setString(4, (String) cmbLevelUbah.getSelectedItem());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            kosong();
            tabelUser();
            lebarKolom();
            txtUsername.requestFocus();
            ubahBarang.dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Diubah" + e);
        }
    }//GEN-LAST:event_btnSimpanUbahActionPerformed

